//using for loop
#include<stdio.h>
int main(){
    int i;
    for ( i = 0; i < 11; i++)
    {
        printf("%d\n", i);
    }
//using while loop
    int j = 11;
    while (j < 21) {
        printf("%d\n", j);
        j++;
    }
//using do while loop
    int k = 21;
    do {
        printf("%d\n", k);
        k++;
    } while (k < 31);
    return 0; 
}



